import type { QaAttestation } from './general-qa';
import type { ProjectId } from './resume-projects';

export interface ProjectQaEntry {
  id: string;
  projectId: ProjectId;
  projectLabel: string;
  category: string;
  question: string;
  answer: string;
  attested?: boolean;
  attestationSource?: string;
}

/**
 * Project Q&A are "attested" by default — each card maps to a project that is
 * documented in the resume (`ResumeProject.attested = true`). Per-entry
 * overrides (attested:false + attestationSource) are respected if present —
 * useful for the occasional "added industry-standard framing" card.
 */
export function getProjectAttestation(entry: ProjectQaEntry): QaAttestation {
  const kind: QaAttestation['kind'] =
    entry.attested === false ? 'imagined' : 'attested';
  const source =
    entry.attestationSource ?? `${entry.projectLabel} · 職務経歴書 Q&A シート`;
  return { kind, source };
}

